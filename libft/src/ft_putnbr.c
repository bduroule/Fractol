#include "../include/libft.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		nb = (-nb);
		ft_putchar('-');
	}
	if (nb == (-2147483648))
	{
		nb = (147483648);
		ft_putchar('2');
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}
