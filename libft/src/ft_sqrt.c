#include "../include/libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (nb >= i * i)
	{
		if (nb == i * i)
		{
			if (nb % i != 0)
				return (0);
			return (i);
		}
		i++;
	}
	return (i);
}
